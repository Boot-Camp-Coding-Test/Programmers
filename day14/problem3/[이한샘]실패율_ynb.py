def solution(N, stages):
    answer = []
    pre_num = 0
    set_list = list(set(stages))
    rate = {}

    if N+1 in set_list:
      set_list.remove(N+1)
    for s in set_list:
      #print(s, stages.count(s)/(len(stages)-pre_num))
      rate[s] = stages.count(s)/(len(stages)-pre_num)
      pre_num += stages.count(s)
    for i in range(1, N+1) :
        if i not in set_list:
          rate[i] = 0
    for x,y in sorted(rate.items(), key=lambda x : x[1], reverse=True):
      answer.append(x)
    return answer

